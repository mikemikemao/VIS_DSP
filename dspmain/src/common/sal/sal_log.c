#include <sal.h>

/* ========================================================================== */
/*                           宏和类型定义区                                   */
/* ========================================================================== */


/* ========================================================================== */
/*                          数据结构定义区                                    */
/* ========================================================================== */

static UINT32 gColorLevel  = SAL_LOG_LV_INFO;

/* ========================================================================== */
/*                          函数定义区                                        */
/* ========================================================================== */

/*******************************************************************************
* 函数名  : SAL_logLevel
* 描  述  : 设置打印等级
* 输  入  : - level: SAL_LogLevel 0 1 2 3
* 输  出  : 无
* 返回值  : HIK_SOK  : 成功
*           HIK_FAIL : 失败
*******************************************************************************/
INT32 SAL_logLevel(UINT32 level)
{
    gColorLevel = level;
    SAL_WARN("Set debug level %d success!\n",gColorLevel);
    return SAL_SOK;
}

/*******************************************************************************
* 函数名  : SAL_logAllowed
* 描  述  : 是否允许打印
* 输  入  : - level: SAL_logAllowed 0 1 2 3
* 输  出  : 无
* 返回值  : SAL_SOK  : 成功
*           SAL_FAIL : 失败
*******************************************************************************/
INT32 SAL_logAllowed(UINT32 level)
{
  if(gColorLevel >= level)
  {
      return SAL_SOK;
  }
  else
  {
      return SAL_FAIL;
  }
}
